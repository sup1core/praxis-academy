public class Aritmatika {
    public static void main (String []args){
        int x=10;
        int y=20;

        int result  = x + y ;
        System.out.println ("x+y= "+result);

        result = x-y;
        System.out.println("x-y= "+result);

        result=x*y;
        System.out.println("x*y= "+result);

        result=x/y;
        System.out.println("x/y= "+result);

        result=x%y;
        System.out.println("x%y= "+result);
    }
}